#include "libft.h"

int	ft_atoi(const char *s)
{
	long int	res;
	int			sign;

	res = 0;
	sign = 1;
	while ((*s == 32) || ((9 <= *s) && (*s <= 13)))
		s++;
	if (*s == 45 || *s == 43)
	{
		if (*s == 45)
			sign = -sign;
		s++;
	}
	while ((48 <= *s) && (*s <= 57))
	{
		res = (res * 10) + (*s - 48);
		s++;
	}
	return (res * sign);
}
